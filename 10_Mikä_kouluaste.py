# Kirjoita ohjelma, joka ensin kysyy käyttäjältä, minkä ikäinen hän on. Jos
# käyttäjä syöttää luvun, joka on välillä 7-12, ohjelma tulostaa 'Olet alakoulussa.'
# Jos ikä on 13-15, tulostetaan 'Olet yläkoulussa'. Jos ikä on 16 tai enemmän, tulostetaan
# 'Olet jo melko vanha'. Jos ikä on pienempi kuin 7, tulostetaan 'Olet päiväkodissa.'

# Lue tässä käyttäjältä ikä
ika = 0

# Varmistetaan että ika on kokonaisluku ennen vertailua
ika = int(ika)

# Päättele iän perusteella mitä tulostetaan
if ika < 7:
    print()
elif ika <= 8:
    print()
elif ika <= 9:
    print()
else:
    print()
